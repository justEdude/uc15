    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          try{
            
       funcionario funcionario = new funcionario();
        
        funcionario.setNome(campoNome.getText());
        funcionario.setContato(Integer.parseInt(campoContato.getText()));
        funcionario.setCPF(Integer.parseInt(campoCPF.getText()));
        funcionario.setEndereco(campoEndereco.getText());
        funcionario.setFuncao(campoFuncao.getText());
                
        
        funcionariosDAO fornecer = new funcionariosDAO();
        fornecer.salvar(funcionario);
       
        JOptionPane.showMessageDialog(null, "Cadastro inserido com sucesso");
        
       campoNome.setText("");
       campoContato.setText("");
       campoCPF.setText("");
       campoEndereco.setText("");
       campoFuncao.setText("");
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "erro ao cadastrar, se certifique de que inseriu o contato e o cpf usando apenas numeros");
        
       campoNome.setText("");
       campoContato.setText("");
       campoCPF.setText("");
       campoEndereco.setText("");
       campoFuncao.setText("");
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

