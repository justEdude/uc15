    private void botaoConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoConfirmarActionPerformed
          
        if(campoInicioTurno == null || campoFimTurno == null){
            
            JOptionPane.showMessageDialog(null, "erro ao cadastrar, por favor se certifique de que o campo data esta no formato dd-MM-y e que o campo inicio e fim estao no formato HH:mm:ss ");
       
        }else{
        
        try{
            
        turnos funcionario = new turnos();
        
        
        funcionario.setNome(campoNome.getText());
        funcionario.setPeriodo(campoPeriodo.getText());
        funcionario.setData(LocalDate.parse(campoData.getText(), DateTimeFormatter.ofPattern("dd-MM-y")));
        funcionario.setInicio(LocalTime.parse(campoInicioTurno.getText(), DateTimeFormatter.ofPattern("HH:mm:ss")));
        funcionario.setFim(LocalTime.parse(campoFimTurno.getText(), DateTimeFormatter.ofPattern("HH:mm:ss")));
        
        turnosDAO fornecer = new turnosDAO();
        fornecer.salvar(funcionario);
       
        JOptionPane.showMessageDialog(null, "Cadastro inserido com sucesso");
        
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "erro ao cadastrar, por favor se certifique de que o campo data esta no formato dd-MM-y e que o campo inicio e fim estao no formato HH:mm:ss");
        }
        }
    }//GEN-LAST:event_botaoConfirmarActionPerformed

