    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try{
            
        vendas venda = new vendas();
        
        venda.setCliente(campoCliente.getText());
        venda.setValor(campoValor.getText());
        venda.setCPF(Integer.parseInt(campoCpf.getText()));
        venda.setDataVenda(LocalDate.parse(campoData.getText(), DateTimeFormatter.ofPattern("dd-MM-y")));
        venda.setHoraVenda(LocalTime.parse(campoHora.getText(), DateTimeFormatter.ofPattern("HH:mm:ss")));
                
        
        vendasDAO vendas = new vendasDAO();
        vendas.salvar(venda);
       
        JOptionPane.showMessageDialog(null, "Cadastro inserido com sucesso");
        
       campoCliente.setText("");
       campoValor.setText("");
       campoCpf.setText("");
       campoData.setText("");
       campoHora.setText("");
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "erro ao cadastrar, se certifique de que inseriu apenas numeros nos campos de cpf, data e hora");
        
       campoCliente.setText("");
       campoValor.setText("");
       campoCpf.setText("");
       campoData.setText("");
       campoHora.setText("");
        
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

